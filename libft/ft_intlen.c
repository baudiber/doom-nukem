#include "libft.h"

size_t	ft_intlen(int nb)
{
	size_t	i;

	i = 0;
	if (nb == 0)
		i = 1;
	if (nb < 0)
	{
		i++;
		nb = -nb;
	}
	while (nb)
	{
		i++;
		nb = nb / 10;
	}
	return (i);
}
